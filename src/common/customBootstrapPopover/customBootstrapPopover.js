angular.module( 'ngUnicityEnrollment.bootstrap.popover', [ 'ngUnicityEnrollment.bootstrap.tooltip' ] )

.directive( 'popoverHtmlPopup', function () {
  return {
    restrict: 'EA',
    replace: true,
    scope: { title: '@', content: '@', image: '@', placement: '@', animation: '&', isOpen: '&' },
    templateUrl: 'template/popover/popover-html.html'
  };
})

.directive( 'popoverHtml', [ '$compile', '$timeout', '$parse', '$window', '$customtooltip', function ( $compile, $timeout, $parse, $window, $customtooltip ) {
  return $customtooltip( 'popoverHtml', 'popover', 'click' );
}]);

angular.module("template/popover/popover-html.html", []).run(["$templateCache", function($templateCache) {
  $templateCache.put("template/popover/popover-html.html",
    "<div class=\"popover {{placement}}\" ng-class=\"{ in: isOpen(), fade: animation() }\">\n" +
    "  <div class=\"arrow\"></div>\n" +
    "\n" +
    "  <div class=\"popover-inner\">\n" +
    "      <h3 class=\"popover-title\" ng-bind-html=\"title\" ng-show=\"title\"></h3>\n" +
    "      <div class=\"text-center\" ng-if=\"image\"><img ng-src=\"{{image}}\"></div>" +
    "      <div class=\"popover-content\" ng-bind-html=\"content\"></div>\n" +
    "  </div>\n" +
    "</div>\n" +
    "");
}]);
